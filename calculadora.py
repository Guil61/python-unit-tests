import math

class Calculadora:
    def somar(self, x, y):
        return x + y
    
    def dividir(self, x,y):
        return x/y
    
    def subtrair(self, x,y):
        return x-y
    
    def multiplicar(self, x, y):
        return x * y
    
    def raizQuadrada(self, x):
        return math.sqrt(x)
    
    def exponenciacao(self, x, y):
        return x ** y
